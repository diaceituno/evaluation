package entities;

public class Poll {

	private int id;
	private String pollName;
	private String fxml;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPollName() {
		return pollName;
	}
	public void setPollName(String pollName) {
		this.pollName = pollName;
	}
	public String getFxml() {
		return fxml;
	}
	public void setFxml(String fxml) {
		this.fxml = fxml;
	}
	
	
}
